package kr.co.ict3.brd.mbr;

public class PageDTO {

	private String maxPage;
	private String reqPage;
	private String beginPage;
	private String endPage;

	public String getMaxPage() {
		return maxPage;
	}
	public void setMaxPage(String maxPage) {
		this.maxPage = maxPage;
	}
	public String getReqPage() {
		return reqPage;
	}
	public void setReqPage(String reqPage) {
		this.reqPage = reqPage;
	}
	public String getBeginPage() {
		return beginPage;
	}
	public void setBeginPage(String beginPage) {
		this.beginPage = beginPage;
	}
	public String getEndPage() {
		return endPage;
	}
	public void setEndPage(String endPage) {
		this.endPage = endPage;
	}

}//class
